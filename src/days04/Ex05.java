package days04;

/*
	1) 조건문
	2) 분기문
	3) 반복문 = for문, foreach문
	

 */

public class Ex05 {

	public static void main(String[] args) {

		
//		for문
//		1. 초기식 : 어떤 변수가 값으로 초기화하는 식
//		2. 조건식 : 참 
//				ㄴ { 명령코딩 블럭 실행 }
//				ㄴ 증감식 : 증가, 감소식
//				  거짓   //거짓이면 반복문을 빠져나감(참이면 무한루프)
		
//			순서: 조건이 맞으면 {}블럭 실행, 증감식이 실행 - 반복
		
		for (int i = 1 ; i <= 10 ; i++) {
			System.out.println(i);
			
		} //for
	}

}
