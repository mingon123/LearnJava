public class Ex05 {

	public static void main(String[] args) {
		// 드래그하고 crtl + alt + 방향키를 누르면 그 내용 복사됨
		// alt + shift + a = 단어 전체 바꿀때(같은 함수 전체 드래그 하면 그거 다 삭제가능)
		
		//print() + 개행
		System.out.println("오재문");
		System.out.println("이찬희");
		System.out.println("양희동");
		System.out.println( );
		
		//print() 
		System.out.print("오재문\n");
		System.out.print("이찬희" + '\n');
		System.out.print("양희동");
		
		System.out.println( ); //개행
		System.out.println("END");
		

		
		
	}

}
